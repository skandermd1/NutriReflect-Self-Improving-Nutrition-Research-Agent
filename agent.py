class reflexion(BaseModel):
    missing: str = Field(description="The missing ideas")
    superfluous: str = Field(description="The superfluous ideas")
class AnswerQuestion(BaseModel):
    Answer: str = Field(description="The final answer to the user's question, incorporating the main response and insights from the reflection.")
    relexion: reflexion = Field(description="The detailed response, reflection, and search queries generated by the agent.")
    search_queries: List[str] = Field(description="A list of 1-3 search queries for further research on antinutrients, bioavailability studies, or evolutionary nutrition.")

initial_chain=responder_prompt|llm.bind_tools([tavily_tool])
def execute_tools(state: List[BaseMessage]) -> List[BaseMessage]:
    last_ai_message = state[-1]
    tool_messages = []
    for tool_call in last_ai_message.tool_calls:
        if tool_call["name"] in ["AnswerQuestion", "ReviseAnswer"]:
            call_id = tool_call["id"]
            search_queries = tool_call["args"].get("search_queries", [])
            query_results = {}
            for query in search_queries:
                result = tavily_tool.invoke(query)
                query_results[query] = result
            tool_messages.append(ToolMessage(
                content=json.dumps(query_results),
                tool_call_id=call_id)
            )
    return tool_messages
